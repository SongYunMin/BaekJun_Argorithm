#include <stdio.h>

int main(void)
{
	int count = 0 , temp = 0;			
	int Static_money;		// 고정 비용
	int Dynamic_money;		// 가변 비용
	int NoteBook_Price;		// 노트북 비용

	scanf("%d %d %d", &Static_money, &Dynamic_money, &NoteBook_Price);
	
	while (temp <= (Static_money + Dynamic_money))
	{
		//dasdsad
		temp += NoteBook_Price;
		count++;
	}

	printf("%d", count);



	return 0;
}